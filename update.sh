#!/usr/bin/env bash
# =============================================================================
#  Lolzteam Workflow Builder — update
#  Запуск:  sudo bash /opt/lzt-builder/update.sh
# =============================================================================
set -euo pipefail

INSTALL_DIR="/opt/lzt-builder"
BRANCH="main"
COMPOSE="docker compose"

R=$'\033[0m'
B=$'\033[1m'
DIM=$'\033[2m'
RED=$'\033[31m'
GRN=$'\033[32m'
CYN=$'\033[36m'
WHT=$'\033[97m'
G=$'\033[38;5;46m'
GY=$'\033[38;5;244m'

die()  { echo -e "${RED}✖${R} $*" >&2; exit 1; }
ok()   { echo -e "${GRN}✔${R} $*"; }
info() { echo -e "${CYN}›${R} $*"; }

banner() {
  # ASCII Lolzteam eye (. - = | _ # @) + brand text
  local EY=$'\033[38;5;46m'
  local rows=(
    "                                          ---------"
    "                                          |=======|"
    "                                 ---------===@#@===---"
    "                                 |===========#@#=====|"
    "                        ---------===#@#======@#@#@#==|"
    "                        |===========@#@======#@#@#@==|"
    "                        |==@#@======#@#@#@#@#@#@#@#===---"
    "                        |==#@#======@#@#@#@#@#@#@#@=====|"
    "               ---------===@#@===@#@#@#@#@#@#@#@#@#@#@==|"
    "               |===========#@#===#@#@#@#@#@#@#@#@#@#@#==|"
    "      ---------===#@#===#@#@#@#@#@#@#@#@#@#@#@#@#@#@#@==|"
    "      |===========@#@===@#@#@#@#@#@#@#@#@#@#@#@#@#@#@#==|"
    "   ---===@#@===@#@#@#===#@#@#@#@#@#@#@#@#@#@#@#@#@#=====|"
    "   |=====#@#===#@#@#@===@#@#@#@#@#@#@#@#@#@#@#@#@#@===___"
    "   |==#@#@#@===@#@#@#@#@#@#@#@#@#@#@#@#@#@#@#========|"
    "   |==@#@#@#===#@#@#@#@#@#@#@#@#@#@#@#@#@#@#@___=====|"
    "   |==#@#@#@#@#@#@#@#@#@#@#@#@#@#@#@#@#=====|   |====|"
    "   |==@#@#@#@#@#@#@#@#@#@#@#@#@#@#@#@#@=====|   |====|"
    "---===#@#@#@#@#@#@#@#@#@#@#@#@. . . . . . #@#@#@#@#===---"
    "|=====@#@#@#@#@#@#@#@#@#@#@#@# . . . . . .@#@#@#@#@=====|"
    "|==@#@#@#@#@#@#@#@#@#@#@. . . . .@#@. . . #@#@#@#@#@#@===---"
    "|==#@#@#@#@#@#@#@#@#@#@# . . . . #@# . . .@#@#@#@#@#@#=====|"
    "|==@#@#@#@#@#@#@#@. . . . . . #@#@#@. . . #@#@#@#@#@#@#@#==|"
    "|==#@#@#@#@#@#@#@# . . . . . .@#@#@# . . .@#@#@#@#@#@#@#@==|"
    "|==@#@#@#@#@======. .@#@. . . #@#@#@. . . #@#@#@#@#@#@=====|"
    "|==#@#@#@#@#===___ . #@# . . .@#@#@# . . .@#@#@#@#@#@#===___"
    "|==@#@========|   #@#@#@. . . . . . . .@#@#@#@#@#@#=====|"
    "|==#@#===______   @#@#@# . . . . . . . #@#@#@#@#@#@===___"
    "|=======|      ---#@#@#@#@#@#@#@#@#@#@#@#@#@#========|"
    "_________      |==@#@#@#@#@#@#@#@#@#@#@#@#@#@===______"
    "               |=======|   @#@#@#@#@#@#========|"
    "               _________   #@#@#@#@#@#@===______"
    "                        ---==============|"
    "                        __________________"
  )
  local row ch i=0
  local mid=17
  printf '\n'
  for row in "${rows[@]}"; do
    printf '  '
    while IFS= read -r -n1 ch; do
      [[ -z "$ch" ]] && continue
      case "$ch" in
        ' ') printf ' ' ;;
        *) printf '%s%s%s' "$EY" "$ch" "$R" ;;
      esac
    done <<<"$row"
    if [[ $i -eq $mid ]]; then printf '    %s%sLolzteam%s' "$WHT" "$B" "$R"; fi
    if [[ $i -eq $((mid+1)) ]]; then printf '    %sWorkflow Builder%s' "$DIM" "$R"; fi
    printf '\n'
    i=$((i+1))
  done
  printf '\n'
}







[[ "${EUID}" -eq 0 ]] || die "Запустите: sudo bash update.sh"
[[ -d "${INSTALL_DIR}/.git" ]] || die "Не найден ${INSTALL_DIR} — сначала install.sh"

banner
cd "$INSTALL_DIR"

info "git pull (${BRANCH})"
git fetch --all --prune
git checkout "$BRANCH"
git pull --ff-only origin "$BRANCH"
ok "код обновлён"

info "docker compose build"
$COMPOSE build
ok "образы собраны"

info "docker compose up -d"
$COMPOSE up -d
ok "сервисы перезапущены"

HTTP_PORT="$(grep -E '^HTTP_PORT=' .env 2>/dev/null | cut -d= -f2 || echo 80)"
HTTP_PORT="${HTTP_PORT:-80}"

if curl -fsS "http://127.0.0.1:${HTTP_PORT}/health" >/dev/null 2>&1; then
  ok "health OK → http://127.0.0.1:${HTTP_PORT}"
else
  info "health ещё прогревается — логи: docker compose logs -f api"
fi

echo
ok "обновление завершено"
echo
